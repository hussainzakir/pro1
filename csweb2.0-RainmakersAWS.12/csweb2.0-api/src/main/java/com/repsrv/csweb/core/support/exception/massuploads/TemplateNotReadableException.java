package com.repsrv.csweb.core.support.exception.massuploads;

public class TemplateNotReadableException extends Exception {

	public TemplateNotReadableException() {
		super();
	}

	public TemplateNotReadableException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TemplateNotReadableException(String message, Throwable cause) {
		super(message, cause);
	}

	public TemplateNotReadableException(String message) {
		super(message);
	}

	public TemplateNotReadableException(Throwable cause) {
		super(cause);
	}

}
