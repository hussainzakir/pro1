package com.repsrv.csweb.core.support.exception.recordings;

public class AddRecordingException extends Exception {

	public AddRecordingException() {
		super();
	}

	public AddRecordingException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public AddRecordingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AddRecordingException(String arg0) {
		super(arg0);
	}

	public AddRecordingException(Throwable arg0) {
		super(arg0);
	}
	
}
