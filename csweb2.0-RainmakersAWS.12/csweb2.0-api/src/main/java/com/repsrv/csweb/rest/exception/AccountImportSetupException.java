package com.repsrv.csweb.rest.exception;

public class AccountImportSetupException extends Exception {

	public AccountImportSetupException() {
		super();
	}

	public AccountImportSetupException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public AccountImportSetupException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AccountImportSetupException(String arg0) {
		super(arg0);
	}

	public AccountImportSetupException(Throwable arg0) {
		super(arg0);
	}

}
